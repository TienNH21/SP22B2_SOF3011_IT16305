package beans.form_data;

public class RegisterData {
	String fullname;
	String email;
	String password;
	String sdt;
	String dia_chi;
	int gioi_tinh;
	int loai_tai_khoan;

	public String getFullname() {
		return fullname;
	}
	public void setFullname(String fullname) {
		this.fullname = fullname;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getSdt() {
		return sdt;
	}
	public void setSdt(String sdt) {
		this.sdt = sdt;
	}
	public String getDia_chi() {
		return dia_chi;
	}
	public void setDia_chi(String dia_chi) {
		this.dia_chi = dia_chi;
	}
	public int getGioi_tinh() {
		return gioi_tinh;
	}
	public void setGioi_tinh(int gioi_tinh) {
		this.gioi_tinh = gioi_tinh;
	}
	public int getLoai_tai_khoan() {
		return loai_tai_khoan;
	}
	public void setLoai_tai_khoan(int loai_tai_khoan) {
		this.loai_tai_khoan = loai_tai_khoan;
	}
	
}
